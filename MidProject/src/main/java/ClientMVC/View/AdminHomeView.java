    private void btnApplyBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyBookingActionPerformed
        // Fetch the table model
        DefaultTableModel model = (DefaultTableModel) tableViewbooking.getModel();

        // Iterate through the table rows
        for (int i = 0; i < model.getRowCount(); i++) {
            int vehicleId = Integer.parseInt(model.getValueAt(i, 0).toString());
            boolean accept = (boolean) model.getValueAt(i, 6); // Accept checkbox
            boolean decline = (boolean) model.getValueAt(i, 7); // Decline checkbox

            // Check if both checkboxes are ticked for the same row
            if (accept && decline) {
                // If both checkboxes are selected, deselect the previous one
                model.setValueAt(false, i, 6); // Uncheck accept checkbox
                model.setValueAt(false, i, 7); // Uncheck decline checkbox
                JOptionPane.showMessageDialog(this, "Please select either Accept or Decline", "Both checkbox are ticked!", JOptionPane.WARNING_MESSAGE);
            } else if (accept) {
                String decision = "accept";
                // If the accept checkbox is ticked, approve the vehicle
                Controller.getInstance().approveVehicle(String.valueOf(vehicleId),decision);
                // Uncheck the decline checkbox for the same row
                model.setValueAt(false, i, 7);
            } else if (decline) {
                // If the decline checkbox is ticked, deny the vehicle
                String decision = "decline";
                Controller.getInstance().denyVehicle(String.valueOf(vehicleId),decision);
                // Uncheck the accept checkbox for the same row
                model.setValueAt(false, i, 6);
            }
        }

        // After processing all rows, refresh the pending requests
        Controller.getInstance().refreshPendingRequests();
    }//GEN-LAST:event_btnApplyBookingActionPerformed

