    private void bookBTNadminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookBTNadminActionPerformed
        SwingUtilities.invokeLater(() -> {
            int selectedRow = bookingTable.getSelectedRow();
            if (selectedRow != -1) {
                String vehicleModel = (String) bookingTable.getValueAt(selectedRow, 1);
                String plateNo = (String) bookingTable.getValueAt(selectedRow, 3);
                Date from = jCalendar2.getDate();
                Date To = jCalendar1.getDate();
                Boolean status = Controller.getInstance().bookForPendingVehicleAdmin(vehicleModel, plateNo,from,To);
                try {
                    if (status) {
                        DefaultTableModel model = (DefaultTableModel) bookingTable.getModel();
                        model.removeRow(selectedRow);
                    }
                }catch (NullPointerException Ignore) {
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please select a vehicle to cancel.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        });
    }//GEN-LAST:event_bookBTNadminActionPerformed

