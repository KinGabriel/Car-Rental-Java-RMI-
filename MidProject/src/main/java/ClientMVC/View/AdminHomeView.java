    private void btnApplyUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyUserActionPerformed
        // Fetch the table model
        DefaultTableModel model = (DefaultTableModel) getManageUserTable().getModel();

        // Iterate through the table rows
        for (int i = 0; i < model.getRowCount(); i++) {
            String user =model.getValueAt(i, 0).toString();
            String status =model.getValueAt(i, 6).toString();
            Boolean accept = (Boolean) model.getValueAt(i, 7); // Accept checkbox
            Boolean decline = (Boolean) model.getValueAt(i, 8); // Decline checkbox

            // Check if both checkboxes are ticked for the same row
            if (accept != null && decline != null && accept && decline) {
                JOptionPane.showMessageDialog(this, "Please select either Ban or Unban", "Both checkboxes are ticked!", JOptionPane.WARNING_MESSAGE);
                // Skip processing this row
                continue;
            } else if (accept != null && accept) {
                // If the accept checkbox is ticked, approve the vehicle
                String decision = "Ban";
                Controller.getInstance().banUser(user, decision,status);
            } else if (decline != null && decline) {
                // If the decline checkbox is ticked, deny the vehicle
                String decision = "Unban";
                Controller.getInstance().unbanUser(user, decision,status);
            }

            // Reset checkboxes for the processed row
            model.setValueAt(false, i, 7); // Uncheck accept checkbox
            model.setValueAt(false, i, 8); // Uncheck decline checkbox
        }
        // After processing all rows, refresh the pending requests
        Controller.getInstance().refreshClientData();
    }//GEN-LAST:event_btnApplyUserActionPerformed

